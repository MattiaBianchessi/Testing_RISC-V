// Utilità -> stampa la soluzione
void printSolution(int sol[N][N])
{
    for (int i = 0; i < N; i++)
    {
        for (int j = 0; j < N; j++)
            printf(" %d ", sol[i][j]);
        printf("\n");
    }
}

// Utilità -> controlla se il movimento è valido
bool isSafe(int maze[N][N], int x, int y)
{
    // if (x, y fuori) return false
    if (x >= 0 && x < N && y >= 0 && y < N && maze[x][y] == 1)
        return true;
    return false;
}

// Risolvi
bool solveMaze(int maze[N][N])
{
    int sol[N][N];
    for(int i = 0; i < N; i ++){
        for (int j = 0; j < N; j++)
        {
            sol[i][j] = 0;
        }
    }
    if (solveMazeUtil(maze, 0, 0, sol) == false)
    {
        printf("Soluzioone non esiste");
        return false;
    }
    printSolution(sol);
    return true;
}

// Utilità -> movimento nel labirinto.
/* tenta il movimento in una direzione. 
*   Se la posizione è valida allora continua ricosivamente nell ordine:
* (->) destra ,(V) giù , (^)su , (<-) sisistra.
* se la posizione di movimento non è valida viene azzerata.
* se mi trovo in posizione [ N-1], [N-1] (la posizione di goal) allora ho trovato la soluzione
*/
bool solveMazeUtil(int maze[N][N], int x, int y, int sol[N][N])
{
    // if (x, y == goal) return true
    if (x == N - 1 && y == N - 1 && maze[x][y] == 1)
    {
        sol[x][y] = 1;
        return true;
    }

    // controllo 
    if (isSafe(maze, x, y) == true)
    {
        
        if (sol[x][y] == 1)
            return false;
       
        sol[x][y] = 1;

        /* Move right */
        if (solveMazeUtil(maze, x + 1, y, sol) == true)
            return true;
        /* Move down */
        if (solveMazeUtil(maze, x, y + 1, sol) == true)
            return true;
        /* Move up */
        if (solveMazeUtil(maze, x, y - 1, sol) == true)
            return true;
        /* Move left */
        if (solveMazeUtil(maze, x - 1, y, sol) == true)
            return true;

        // nessuna mossa then
        // BACKTRACK: azzero 
        sol[x][y] = 0;
        return false;
    }
    return false;
}


